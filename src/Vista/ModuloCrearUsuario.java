    private void btnFiltrarUMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnFiltrarUMouseClicked
        

       if(this.jrbTodosU.isSelected())    
       {   
           cus.limpiarTabla(this);
           cus.ConsultarTodosLosUsuarios(this);
           
       }else if(this.jrbCantidadU.isSelected())
       {
           
           cus.limpiarTabla(this);
           cus.consultarCantidadDeUsuarios(this);
           
       }else if(this.jrbEstadoActualU.isSelected())
       {
           
           cus.limpiarTabla(this);
           cus.consultarUsuariosPorEstado(this);
           
       }else if(this.jrbIdUsuario.isSelected())
       {
           
           cus.limpiarTabla(this);
           cus.consultarUsuariosPorId(this);
           
       }else if(this.jrbNombreU.isSelected())
       {
           
           cus.limpiarTabla(this);
           cus.consultarUsuariosPorNombre(this);
           
       }else if(this.jrbPrivilegioU.isSelected())
       {
           
           cus.limpiarTabla(this);
           cus.consultarUsuariosPorPrivilegio(this);
           
       }
            
       

    }//GEN-LAST:event_btnFiltrarUMouseClicked

