    private void btnInsertarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnInsertarMouseClicked

        boolean estadoCampos;    
        usuario = new Usuario();
    
        estadoCampos = cus.obtenerinformación(this);
        
        if(estadoCampos = false)
        {
            
          JOptionPane.showMessageDialog(null, "Inserta Información verídica");
            
        }else
        {
        
        SQLUsuarios.insertarUsuario(usuario);
        
        if(usuario.getIdUsuario() > 0)
        {
            cus.cargarTablaUsuarios(this);
        }

        }
        
    }//GEN-LAST:event_btnInsertarMouseClicked

